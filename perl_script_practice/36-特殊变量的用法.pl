#$$ Perl解释器的进程ID
#$% 当前输出通道的当前页号
#$& 与上个格式匹配的字符串
#$( 当前进程的组ID$) 当前进程的有效组ID
#$* 设置1表示处理多行格式.现在多以/s和/m修饰符取代之.
