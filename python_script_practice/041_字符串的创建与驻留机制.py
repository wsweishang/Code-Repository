########################################041_字符串的创建与驻留机制########################################
#字符串的驻留是指：对相同的字符串只保留一份拷贝，后续创建相同字符串时将该字符串的内存地址赋给新创建的变量
#字符串的驻留条件（交互式）：
#1、字符串长度为0或1
#2、符合标识符（字母数字下划线）的字符串
#3、字符串指在编译而非运行时进行驻留
#4、-5到256之间的整数数字
#sys.intern()指定驻留

a = ""
b = ""
print(a == b)
print(a is b)

a = "1"
b = "1"
print(a == b)
print(a is b)

a = "abc%"
b = "abc%"
print(a == b)
print(a is b)















