########################################13_位运算符########################################
#位与&：对应数位都是1，结果数位才是1，否则为0
#位或|：对应数位都是0，结果数位才是0，否则为1
#异或^：对应的数字相同为0，相反为1
#取反~：取反
#左移位运算符<<：高位溢出舍弃，低位补0，相当于乘2
#右移位运算符>>：低位溢出舍弃，高位补0，相当于除2

print(4 & 8)
print(4 | 8)
print(4 ^ 8)

print(4<<2)
print(4<<1)
print(4>>1)
print(4>>2)


































