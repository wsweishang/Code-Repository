########################################16_对象的布尔值########################################
#在python中任何对象都有自己的布尔值，bool()
#False、数值0、None、空字符串、空列表、空元组、空字典、空集合的布尔值为False
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(None))
print(bool(''), bool(""))
print(bool([]), bool(list()))
print(bool(()), bool(tuple()))
print(bool({}), bool(dict()))
print(bool(set()))








